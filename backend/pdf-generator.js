// Pipedream workflow: PDF Generator with Puppeteer
// Trigger: HTTP API (from gemini-analysis)
// Purpose: Generate PDF and save to R2

import puppeteer from '@pipedream/puppeteer';

export default defineComponent({
  async run({ steps, $ }) {
    const { contactId, reportData } = steps.trigger.event.body;

    // Launch Puppeteer
    const browser = await puppeteer.launch();
    const page = await browser.newPage();

    try {
      // Set content with professional styling
      const htmlContent = `
        <!DOCTYPE html>
        <html>
        <head>
          <meta charset="UTF-8">
          <style>
            body { 
              font-family: 'Inter', 'Helvetica', 'Arial', sans-serif; 
              line-height: 1.6; 
              color: #333; 
              max-width: 800px; 
              margin: 0 auto; 
              padding: 40px 20px;
            }
            .header { 
              text-align: center; 
              border-bottom: 3px solid #10B981; 
              padding-bottom: 20px; 
              margin-bottom: 30px;
            }
            h1 { color: #065F46; font-size: 28px; margin-bottom: 10px; }
            h2 { color: #065F46; font-size: 22px; margin-top: 25px; margin-bottom: 15px; }
            h3 { color: #047857; font-size: 18px; margin-top: 20px; margin-bottom: 10px; }
            .section { margin-bottom: 25px; }
            .recommendation { 
              background: #F0FDF4; 
              padding: 15px; 
              border-left: 4px solid #10B981; 
              margin: 15px 0; 
            }
            .footer { 
              margin-top: 40px; 
              text-align: center; 
              font-size: 12px; 
              color: #6B7280; 
              border-top: 1px solid #E5E7EB; 
              padding-top: 20px;
            }
          </style>
        </head>
        <body>
          <div class="header">
            <h1>Personalized AI Habit Blueprint</h1>
            <p>Generated on ${new Date(reportData.generatedAt).toLocaleDateString()}</p>
          </div>
          <div id="content">
            ${reportData.htmlContent}
          </div>
          <div class="footer">
            <p>Confidential Report for ${reportData.contactId} â€¢ Generated by Habit Mastery System</p>
          </div>
        </body>
        </html>
      `;

      await page.setContent(htmlContent, { waitUntil: 'networkidle0' });
      
      // Generate PDF
      const pdfBuffer = await page.pdf({
        format: 'A4',
        printBackground: true,
        margin: { top: '1cm', right: '1cm', bottom: '1cm', left: '1cm' }
      });

      // Save PDF to R2
      await saveToR2(
        `reports/${contactId}/report.pdf`,
        pdfBuffer,
        'application/pdf'
      );

      // Trigger email sender
      await $send.http({
        method: 'POST',
        url: 'https://your-pipedream-email-workflow.pipedream.net',
        data: { contactId, reportData }
      });

      return { status: 'pdf_generated' };

    } finally {
      await browser.close();
    }
  },
})
